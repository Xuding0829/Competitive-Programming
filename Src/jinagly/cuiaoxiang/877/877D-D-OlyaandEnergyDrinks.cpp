const int d4[4][2] = {{1, 0}, {0, -1}, {-1, 0}, {0, 1}};

int main() {
  int n, m, len;
  cin >> n >> m >> len;
  vector<string> s(n);
  for (int i = 0; i < n; ++i) cin >> s[i];

  int x1, y1, x2, y2;
  cin >> x1 >> y1 >> x2 >> y2;
  --x1; --y1; --x2; --y2;

  queue<ii> Q;
  Q.push({x1, y1});
  auto d = vect<int>(inf<int>, n, m);
  d[x1][y1] = 0;

  while (!Q.empty()) {
    auto [x, y] = Q.front();
    Q.pop();
    for (int k = 0; k < 4; ++k) {
      for (int L = 1; L <= len; ++L) {
        int nx = x + L * d4[k][0];
        int ny = y + L * d4[k][1];
        if (nx < 0 || nx >= n || ny < 0 || ny >= m) break;
        if (s[nx][ny] == '#') break;
        if (d[nx][ny] <= d[x][y]) break;
        if (d[nx][ny] == inf<int>) {
          d[nx][ny] = d[x][y] + 1;
          Q.push({nx, ny});
        }
      }
    }
  }

  int ret = d[x2][y2];
  if (ret == inf<int>) ret = -1;
  cout << ret << '\n';
  return 0;
}

