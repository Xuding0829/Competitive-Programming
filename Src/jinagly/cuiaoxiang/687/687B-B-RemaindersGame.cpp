const int N = 1e6 + 10;
bool flag[N];
vector<int> prime;
int minp[N];

void prime_gen(int n) {
  fill(flag, flag + n, false);
  for (int i = 2; i < n; ++i) {
    if (!flag[i]) {
      prime.push_back(i);
      minp[i] = i;
    }
    for (int j = 0, k; j < SZ(prime) && (k = prime[j] * i) < n; ++j) {
      flag[k] = true;
      if (i % prime[j]) {
        minp[k] = prime[j];
      } else {
        minp[k] = minp[i];
        break;
      }
    }
  }
  trace(SZ(prime));
}

int main() {
  prime_gen(N);
  int n, m;
  cin >> n >> m;

  auto factor =
    [&](int n) {
      map<int, int> ret;
      for (int t = n; t > 1; ) {
        int p = minp[t];
        ret[p]++;
        t /= p;
      }
      return ret;
    };
  auto fm = factor(m);
  trace(fm);
  map<int, int> cnt;

  for (int i = 0; i < n; ++i) {
    int x;
    cin >> x;
    auto cur = factor(x);
    trace(x, cur);
    for (auto& [k, v] : cur) {
      ckmax(cnt[k], v);
    }
  }
  bool ok = 1;
  for (auto& [k, v] : fm) {
    if (cnt[k] < v) {
      ok = 0;
      break;
    }
  }
  cout << (ok ? "Yes" : "No") << '\n';
  return 0;
}

